/* ====================================================================
   Context + Provider + Hook
   ==================================================================== */

const LanguageContext = createContext(null)

export function LanguageProvider({ children }) {
  const [lang, setLangState] = useState(() => {
    try {
      return localStorage.getItem(STORAGE_KEY) || 'pt-BR'
    } catch {
      return 'pt-BR'
    }
  })

  const setLang = (newLang) => {
    setLangState(newLang)
    try {
      localStorage.setItem(STORAGE_KEY, newLang)
    } catch { /* ignore */ }
  }

  // Atualiza lang do documento para leitores de tela nativos
  useEffect(() => {
    const langCode = translations[lang]?.['lang.code'] || 'pt-BR'
    document.documentElement.lang = langCode
  }, [lang])

  const t = (key) => {
    return translations[lang]?.[key] || translations['pt-BR']?.[key] || key
  }

  return (
    <LanguageContext.Provider value={{ lang, setLang, t }}>
      {children}
    </LanguageContext.Provider>
  )
}

export function useLanguage() {
  const ctx = useContext(LanguageContext)
  if (!ctx) throw new Error('useLanguage must be used within LanguageProvider')
  return ctx
}

export default translations
